#include "libft.h"

int		ft_my_length_n(int n)
{
	int		len;
	long	nb;

	len = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		len++;
		nb = n * -1;
	}
	else
		nb = n;
	while (nb != 0)
	{
		nb /= 10;
		len++;
	}
	return (len);
}
